#ifndef TOKENNUMBERS_H
#define TOKENNUMBERS_H

enum Token {
    ID,         // identifier
    INT,        // integer
    ASSIGN,     // =
    PLUS,       // +
    MINUS,      // -
    TIMES,      // *
    DIVIDE,     // /
    LPAREN,     // (
    RPAREN,     // )
    SEMI,       // ;
    END         // end of input
};

#endif
